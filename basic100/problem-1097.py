GRID_SIZE = 19

def input_parser():
    input_grid = []    
    for i in range(GRID_SIZE):
        row = list(map(int, input().split(" ")))
        input_grid.append(row)
    operation_number = int(input())
    operations = []
    for i in range(operation_number):
        operation = list(map(int, input().split(" ")))
        operations.append(operation)
    return input_grid, operations

def operate_grid(grid, operation):
    # horizontal
    horiz = operation[0]
    for i in range(GRID_SIZE):
        grid[horiz-1][i] = 1-grid[horiz-1][i]
    # vertical
    vert = operation[1] 
    for i in range(GRID_SIZE):
        grid[i][vert-1] = 1-grid[i][vert-1]
    
    return grid

def print_grid(grid):
    for row in grid:
        print(" ".join(map(str, row)))

def main():
    grid, operations = input_parser()
    for operation in operations:
        grid = operate_grid(grid, operation)
    print_grid(grid)

if __name__ == "__main__":
    main()
